package org.example.J1_fundamentos.Fase01_FundamentosSolidos;

public class OperadorTernario {
    static void main() {
        // OPERADOR TERNÁRIO
        // Sintaxe do operador:
        // (Condição) ? se verdadeiro : se falso;

        // Exemplo:
        int idade = 18;
        String status = (idade >= 18) ? "Ok" : "Não";
        System.out.println(status);

    }
}
